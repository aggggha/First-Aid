package org.bekadesain.firstaid;

public class Cases {
    private String casesName;
    private String casesSuggestion;
    private String casesAvoid;

    public String getCasesName() {
        return casesName;
    }

    public void setCasesName(String casesName) {
        this.casesName = casesName;
    }

    public String getCasesSuggestion() {
        return casesSuggestion;
    }

    public void setCasesSuggestion(String casesSuggestion) {
        this.casesSuggestion = casesSuggestion;
    }

    public String getCasesAvoid() {
        return casesAvoid;
    }

    public void setCasesAvoid(String casesAvoid) {
        this.casesAvoid = casesAvoid;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    private String photo;
}
